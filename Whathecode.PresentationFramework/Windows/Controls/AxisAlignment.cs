namespace Whathecode.System.Windows.Controls
{
	/// <summary>
	///   Specifies how an element added to a <see cref="AxesPanel{TX,TXSize,TY,TYSize}" /> should be aligned along the axis.
	/// </summary>
	public enum AxisAlignment
	{
		/// <summary>
		///   The element is positioned before the specified axes value.
		/// </summary>
		BeforeValue,
		/// <summary>
		///   The element is centered at the specified axes value.
		/// </summary>
		Center,
		/// <summary>
		///   The element is positioned after the specified axes value.
		/// </summary>
		AfterValue
	}
}
